#!/usr/bin/env bash

var1='http://www.aaa.com/456/123.html'
# --------------------------------------------------------------------------------
# ⏹ #号截取，删除指定字符左边的字符，保留右边字符。
# --------------------------------------------------------------------------------

# 删除了 // 符号左侧的所有字符串
echo ${var1#*//}  # www.aaa.com/456/123.html
# 删除了 / 符号左侧的所有字符串
echo ${var1#*/}   # /www.aaa.com/456/123.html

# --------------------------------------------------------------------------------
# ⏹ ##号截取，删除指定字符左边的字符，保留右边字符。
#    如果指定的字符有多个的话，就从左边删除到右边最后一个符号
# --------------------------------------------------------------------------------
echo ${var1#*/}   # /www.aaa.com/456/123.html
echo ${var1##*/}  # 123.html

# --------------------------------------------------------------------------------
# ⏹ %号截取，删除右边字符，保留左边字符
# --------------------------------------------------------------------------------
echo ${var1%/*}   # http://www.aaa.com/456

# --------------------------------------------------------------------------------
# ⏹ %%号截取，删除指定字符右边的字符，保留左边字符。
#    如果指定的字符有多个的话，就从右边删除到左边最后一个符号
# --------------------------------------------------------------------------------
echo ${var1%%/*}  # http:

# -----------------------------------------------------------------------------------------------------
# #、## 表示从左边开始删除。一个 # 表示从左边删除到第一个指定的字符；两个 # 表示从左边删除到最后一个指定的字符。
# %、%% 表示从右边开始删除。一个 % 表示从右边删除到第一个指定的字符；两个 % 表示从左边删除到最后一个指定的字符。
# 删除包括了指定的字符本身。
# -----------------------------------------------------------------------------------------------------