#!/usr/bin/env bash

# ----------------------------------------------------------------------------------------------------
# ⏹在脚本开头使用 set -e 的时候，若想要临时设置命令执行失败，脚本继续运行的话
#   除了 使用 set +e 临时关闭 -e 之外，还可以使用
#     command || true
#   这样就算命令执行失败，脚本也会继续运行
# ----------------------------------------------------------------------------------------------------

set -e

# 下面这种写法可以保证，当前面的命令执行失败的时候，结果会是true，从而保证脚本继续运行
not_exit_command || true

echo 'Hello World!'