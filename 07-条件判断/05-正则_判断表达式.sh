#!/usr/bin/env bash

# ---------------------------------------------------------------------------------------------------
# ⏹[[ expression ]]这种判断形式，支持正则表达式。
# 
# [[ string1 =~ regex ]]
# ---------------------------------------------------------------------------------------------------

INT=-5

# -------------------------------------------------
# ⏹=~ 是正则比较运算符
# -------------------------------------------------
if [[ "$INT" =~ ^-?[0-9]+$ ]]; then
    echo "INT变量值是一个数字."
    exit 0
else
    echo "INT变量值不是一个数字." >&2
    exit 1
fi