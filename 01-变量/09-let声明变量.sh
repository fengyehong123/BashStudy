#!/usr/bin/env bash

var1='10'
var2='20'

# shellcheck disable=SC2219
# ----------------------------------------------------------------------------------------------------------------
# ⏹let命令声明变量时，可以直接执行算术表达式。
# 但是不推荐，更推荐使用 ((  )) 来计算
# ----------------------------------------------------------------------------------------------------------------
let result1=$var1*$var2
echo "计算之后的值为：${result1}"  # 计算之后的值为：200

# shellcheck disable=SC2125
# ----------------------------------------------------------------------------------------------------------------
# ⏹使用 declare -i 声明变量的类型为数值
# 但是不推荐，更推荐使用 ((  )) 来计算
# ----------------------------------------------------------------------------------------------------------------
declare -i result2=$var1*$var2
echo "计算之后的值为：${result2}"  # 计算之后的值为：200

# ----------------------------------------------------------------------------------------------------------------
# ⏹更加推荐的写法
# ----------------------------------------------------------------------------------------------------------------
result3=$(( var1*var2 ))
echo "计算之后的值为：${result3}"  # 计算之后的值为：200