#!/usr/bin/env bash

# ----------------------------------------------------------------------------------------------------
# ⏹如果读取数组成员时，没有读取指定哪一个位置的成员，默认使用0号位置。
# ----------------------------------------------------------------------------------------------------
declare -a arr1
# shellcheck disable=SC2178
# ⚠️⚠️⚠️不推荐这么写，此处只是为了展示用法⚠️⚠️⚠️
# 赋值的时候不指定位置，实际上是给 arr1[0] 赋值。
arr1='地瓜'
echo "${arr1[0]}"

# ----------------------------------------------------------------------------------------------------
# ⏹引用一个不带下标的数组变量，则引用的是0号位置的数组元素。
# ----------------------------------------------------------------------------------------------------
declare -a arr2=( {1..5} )
# shellcheck disable=SC2128
# ⚠️⚠️⚠️不推荐这么写，此处只是为了展示用法⚠️⚠️⚠️
# 引用数组元素的时候，没有指定位置，结果返回的是0号位置。
echo "${arr2}"

echo -e "\e[1;31m/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_分割线_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_\e[0m"

# ----------------------------------------------------------------------------------------------------
# ⏹数组的长度
#   ${#array[*]}
#   ${#array[@]}
# ----------------------------------------------------------------------------------------------------
echo "数组的长度为: ${#arr2[@]}"  # 数组的长度为: 5
echo "数组的长度为: ${#arr2[*]}"  # 数组的长度为: 5

# 使用获取数组长度的语法去获取某一个元素的话，获取到的是元素的长度，而不是数组的长度
arr2+=("Hello_World")
echo "元素的长度为: ${#arr2[5]}"  # 11
